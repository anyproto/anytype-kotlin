// Функция расширения для преобразования camelCase в snake_case
fun String.toSnakeCase(): String {
    return fold(StringBuilder()) { acc, c ->
        if (c.isUpperCase()) {
            if (acc.isNotEmpty()) acc.append('_')
            acc.append(c.lowercaseChar())
        } else {
            acc.append(c)
        }
        acc
    }.toString()
}

// Data class для иконки
data class CustomIcon(val rawValue: String) {
    // Например, "batteryDead" -> "ci_battery_dead"
    val drawableName: String
        get() = "ci_" + rawValue.toSnakeCase()

    val vectorName: String
        get() = "Ci" + rawValue.replaceFirstChar { it.uppercase(Locale.getDefault()) }
}

